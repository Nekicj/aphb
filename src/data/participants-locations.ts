// Функция для получения статистики по странам
export function getCountryStats() {
  const countryStats = participantsLocations.reduce((acc, location) => {
    if (!acc[location.country]) {
      acc[location.country] = {
        cities: 0,
        participants: 0
      };
    }
    acc[location.country].cities += 1;
    acc[location.country].participants += location.participants;
    return acc;
  }, {} as Record<string, { cities: number; participants: number }>);

  return Object.entries(countryStats).map(([country, stats]) => ({
    country,
    ...stats
  }));
}

// Функция для получения общей статистики
export function getTotalStats() {
  const totalCities = participantsLocations.length;
  const totalCountries = new Set(participantsLocations.map(location => location.country)).size;
  
  // Возвращаем реальные числа для статистики
  return {
    participants: 700, // Примерное общее количество участников
    cities: totalCities,
    countries: totalCountries
  };
}
